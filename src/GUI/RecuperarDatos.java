    private void ejecutar() {
        try {
            commandMETA_BD.ejecutar(ARCHIVO);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "No existe el archivo META_BD");
        } catch (SinTablasException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (META_BDSobreescritoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (META_BDNuevoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (META_BDRecuperadoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } finally {
            try {
                this.dispose();
                showVentanaPrincipal();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "No existe el archivo META_BD");
            }
        }
    }
    
    private void showVentanaPrincipal() throws IOException {
        Principal ventana = new Principal(ARCHIVO);
        ventana.setVisible(true);
    }
    
